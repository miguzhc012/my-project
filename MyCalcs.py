# Demonstração do uso de operadores...
X = int(input("Digite um número:"))
Y = int(input("Digite outro número:"))

Soma = X + Y 
print ("O resultado da soma é: ", Soma)

Subtração = X-Y
print ("O resultado da subtração é: ", Subtração)

Multiplicação = X * Y
print ("O resultado da multiplicação é:", Multiplicação)

Divisão = X / Y 
print ("O resultado da divisão é:", Divisão)

Quad = X **2
print (f"O resultado de {X} ao quadrado é {Quad}")

print ("Ok!")