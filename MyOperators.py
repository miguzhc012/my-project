print ("O resultado da expressão #1 é: ", Valor) 

Valor = ((Y - X) + (Z - X)) / 3
print ("O resultado da expressão #2 é: ", Valor)

Valor = (X + (Y * 2 + (Z / 10))) - 18
print ("O resultado da expressão #3 é: ", Valor)

